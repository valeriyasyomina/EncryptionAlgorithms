        /// <summary>
        /// Копирует из массива данных, начиная с заданного индекса, 16 байт (4 слова) = размер одного блока данных
        /// </summary>
        /// <returns></returns>
        private byte[] CopyFromDataBlock(int index, byte[] message)
        {
            byte[] blockData = new byte[keySize];

            if (index + keySize <= message.Length)
            {
                for (int i = 0; i < keySize; i++)
                    blockData[i] = message[index + i];
            }
            else
            {
                int j = 0;
                for (int i = 0; i + index < message.Length; i++, j++)
                    blockData[j] = message[index + i];

                if (j == blockData.Length)
                    blockData[j] = 0;
                else
                {
                    blockData[j] = 1;
                    for (j++; j < keySize - 1; j++)
                        blockData[j] = 0;
                    blockData[j] = 1;
                }
            }
            return blockData;
        }

        /// <summary>
        /// Шифрует блок данных по 128 бит
        /// </summary>
        /// <param name="blockData">Блок данных</param>
        /// <returns>Зашифрованный блок данных</returns>
        private byte[] EncipherDataBlock(byte[] blockData)  
        {
            byte[,] encipheredBlockData = FormMatrixFromArray(blockData);

            byte[,] K0 = GetRaundKey(0);
            XORBlockDataWithKey(encipheredBlockData, K0);

            for (int i = 1; i < Nr + 1; i++)
            {
                SubBytes(encipheredBlockData);
                ShiftRowsLeft(encipheredBlockData);
                if (i != Nr)
                {
                    encipheredBlockData = MixColumns(encipheredBlockData);
                }
                byte[,] Ki = GetRaundKey(i * keySize);
                XORBlockDataWithKey(encipheredBlockData, Ki);
            }    
            return FormArrayFromMatrix(encipheredBlockData, blockData.Length);
        }
        /// <summary>
        /// Расшифровывает блок данных по 128 бит
        /// </summary>
        /// <param name="blockData">Блок данных</param>
        /// <returns>Расшифрованный блок данных</returns>
        private byte[] DecipherDataBlock(byte[] blockData)   
        {
            byte[,] decipheredBlockData = FormMatrixFromArray(blockData);

            byte[,] KN = GetRaundKey(Nr * keySize);
            XORBlockDataWithKey(decipheredBlockData, KN);

            for (int i = Nr - 1; i >= 0; i--)
            {
                ShiftRowsRight(decipheredBlockData);
                InvSubBytes(decipheredBlockData);
                byte[,] Ki = GetRaundKey(i * keySize);
                XORBlockDataWithKey(decipheredBlockData, Ki);

                if (i != 0)
                {
                    decipheredBlockData = InvMixColumns(decipheredBlockData);
                }               
            }       
            return FormArrayFromMatrix(decipheredBlockData, blockData.Length);
        }

