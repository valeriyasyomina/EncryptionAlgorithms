        /// <summary>
        /// Зашифровывает сообщение любой длины, переданное в байтах
        /// </summary>
        /// <param name="message">Переданное сообщение</param>
        /// <returns>Зашифрованное сообщение</returns>
        private byte[] EncipherMessage(byte[] message)
        {
            int arraySize = 0;
            if (message.Length % keySize == 0)
                arraySize = message.Length + keySize;
            else            
                arraySize = ((message.Length / keySize) + 1) * keySize;

            byte[] encipheredMessage = new byte[arraySize];
            GenerateRaundKeys();
            for (int i = 0; i < arraySize; i += keySize)
            {
                byte[] copyBlock = CopyFromDataBlock(i, message);
                byte[] encipheredBlock = EncipherDataBlock(copyBlock);

                for (int j = 0; j < keySize; j++)
                    encipheredMessage[i + j] = encipheredBlock[j];
            }
            return encipheredMessage;
        }
