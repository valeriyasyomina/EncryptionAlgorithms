        /// <summary>
        /// Расширяет ключи, то есть создает раундовые ключи по секретному
        /// </summary>
        public void GenerateRaundKeys()
        {            
            int i = 0;
            for (i = 0; i < keySize; i++)
            {
                raundKeys[i] = secretKey[i];
            }
            for (; i < raundKeys.Length; i += Nb)
            {
                if ((i/Nb) % keySize == 0)
                {
                     byte[] previousWord = new byte[Nb];
                     previousWord[0] = raundKeys[i - Nb];
                     previousWord[1] = raundKeys[i - Nb + 1];
                     previousWord[2] = raundKeys[i - Nb + 2];
                     previousWord[3] = raundKeys[i - Nb + 3];

                     byte[] prevKeyWord = new byte[Nb];
                     prevKeyWord[0] = raundKeys[i - Nb * Nb];
                     prevKeyWord[1] = raundKeys[i - Nb * Nb + 1];
                     prevKeyWord[2] = raundKeys[i - Nb * Nb + 2];
                     prevKeyWord[3] = raundKeys[i - Nb * Nb + 3];

                    byte[] curWord = XorWords(XorWords(SubWord(RotateWord(previousWord)), GetRCon(i / (Nb * Nb))) , prevKeyWord);
                    for (int j = 0; j < Nb; j++)
                    {
                        raundKeys[i + j] = curWord[j];
                    }
                }                   
                else
                {
                    for (int j = 0; j < Nb; j++)
                    {
                        raundKeys[i + j] = (byte) (raundKeys[i + j - Nb] ^ raundKeys[i + j - secretKey.Length]);
                    }

