        /// <summary>
        /// Сдвигает строки матрицы, начиная с первой, влево
        /// </summary>
        private void ShiftRowsLeft(byte[,] blockData)
        {
            for (int i = 0; i < Nk; i++)
            {
                for (int k = 0; k < i; k++)
                {
                    byte tmp = blockData[i, 0];
                    for (int j = 0; j < Nb - 1; j++)
                        blockData[i, j] = blockData[i, j + 1];
                    blockData[i, Nb - 1] = tmp;
                }
            }
        }
        /// <summary>
        /// Сдвигает строки матрицы, начиная с первой, вправо
        /// </summary>
        private void ShiftRowsRight(byte[,] blockData)
        {
            for (int i = 0; i < Nk; i++)
            {
                for (int k = 0; k < i; k++)
                {
                    byte tmp = blockData[i, Nb - 1];
                    for (int j = Nb - 1; j > 0; j--)
                        blockData[i, j] = blockData[i, j - 1];
                    blockData[i, 0] = tmp;
                }
            }
        }

        /// <summary>
        /// Перемешивает столбцы внутри матрицы данных
        /// </summary>
        /// <param name="blockData">Матрица данных</param>
        /// <returns>Матрица данных с перемешанными столбцами</returns>
        private byte[,] MixColumns(byte[,] blockData)
        {
            byte[,] result = new byte[Nk, Nb];

            for (int i = 0; i < Nb; i++)
            {
                byte[] column = GetColumnFromMatrix(blockData, i);
                byte[] resultColumn = new byte[Nk];

                for (int j = 0; j < Nk; j++)
                {
                    int tmp = 0;
                    for (int k = 0; k < Nk; k++)
                    {
                        tmp = (byte) (MultGalua(Cx[j, k], column[k]) ^ tmp);
                        if (tmp > 255)
                            resultColumn[j] = (byte)(tmp ^ 0x11B);
                        else
                            resultColumn[j] = (byte) tmp;
                    }
                }
                for (int j = 0; j < Nk; j++)                
                    result[j, i] = resultColumn[j]; 
            }
            return result;
        }
