        private byte[] DecipherShorter(byte[] array)
        {
            if (array.Length > 0)
            {
                int resArrayLen = 0;
                if (array[array.Length - 1] == 0)
                    resArrayLen = array.Length - 1;
                else
                {
                    resArrayLen = array.Length - 2;
                    while (resArrayLen >= 0 && array[resArrayLen] != 1)
                        resArrayLen--;
                }
                if (resArrayLen > 0)
                {
