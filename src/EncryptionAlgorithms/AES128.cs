        private byte[,] FormMatrixFromArray(byte[] array)
        {
            byte[,] matrix = new byte[Nk, Nb];
            for (int i = 0; i < Nk; i++)
                for (int j = 0; j < Nb; j++)
                    matrix[i, j] = array[Nb * j + i];
            return matrix;
